public class Auto {

    private String Marca;
    private String Modelo;
    private int Precio;

    public Auto() {

    }

    public Auto(String mar, String mod, int pre) {
        Marca = mar;
        Modelo = mod;
        Precio = pre;

    }

    public String getMarca() {
        return Marca;
    }

    public void setMarca(string marca) {
        Marca = marca;
    }

    public String getModelo() {
        return Modelo;
    }

    public void setMarca(string mod) {
        Modelo = mod;
    }

    public String getPrecio() {
        return Precio;
    }

    public void setMarca(string pre) {
        Precio = pre;
    }
}